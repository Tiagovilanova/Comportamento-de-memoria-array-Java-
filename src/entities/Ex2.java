package entities;

public class Ex2 {
private String nome;
private double price;
public Ex2(String nome, double price) {
	super();
	this.nome = nome;
	this.price = price;
}
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public double getPrice() {
	return price;
}
public void setPrice(double price) {
	this.price = price;
}

}
